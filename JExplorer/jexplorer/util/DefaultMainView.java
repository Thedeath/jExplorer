package jexplorer.util;

import javax.swing.Icon;
import javax.swing.JPanel;
import org.fseek.plugin.interfaces.MainView;

/**
 *
 * @author sWimmesberger
 */
public class DefaultMainView implements MainView
{
    private JPanel panel;
    private String titel;
    private Icon icon;

    public DefaultMainView(JPanel panel, String titel, Icon icon) {
        this.panel = panel;
        this.titel = titel;
        this.icon = icon;
    }

    /**
     * @return the panel
     */
    public JPanel getPanel() {
        return panel;
    }

    /**
     * @param panel the panel to set
     */
    public void setPanel(JPanel panel) {
        this.panel = panel;
    }

    /**
     * @return the titel
     */
    public String getTitel() {
        return titel;
    }

    /**
     * @param titel the titel to set
     */
    public void setTitel(String titel) {
        this.titel = titel;
    }

    /**
     * @return the icon
     */
    public Icon getIcon() {
        return icon;
    }

    /**
     * @param icon the icon to set
     */
    public void setIcon(Icon icon) {
        this.icon = icon;
    }

    
}
